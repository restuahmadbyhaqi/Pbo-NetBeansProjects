public class perulanganfor {
    public static void main(String[] args) {
        System.out.println("magelang");
        System.out.println("magelang");
        System.out.println("magelang");
        System.out.println("magelang");
        System.out.println("magelang");
        
        System.out.println("========");
        for (int i = 0; i < 10; i++) {
            System.out.println("magelang");
        }
        
        System.out.println("===for lainnya===");
        for (int i = 0; i < 6; i++) {
            System.out.println("magelang " + i);
        }
        
        System.out.println("===for Bersarang===");
        
        int a = 5;
        for (int i = 0; i <= a; i++) {
            for (int j = 0; j <= i; j++) {
                System.out.print("*");
            }
        System.out.println("");
    }
    }
    
}
