    private void btnloadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloadActionPerformed
        String sql = "SELECT * FROM buku";
        
        try {
            Statement stmt = koneksi.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            
            DefaultTableModel model = new DefaultTableModel();
            tabelbuku.setModel(model);

            model.addColumn("ID");
            model.addColumn("Judul");
            model.addColumn("Pengarang");
            model.addColumn("Penerbit");
            model.addColumn("ISSBN");
            model.addColumn("Jumlah Salinan");
            model.addColumn("Ringkasan");
            
            model.setRowCount(0);
            
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_buku"),
                    rs.getString("judul"),
                    rs.getString("pengarang"),
                    rs.getString("penerbit"),
                    rs.getString("issbn"),
                    rs.getInt("jumlah_salinan"),
                    rs.getString("ringkasan")
                });
            }
            
            tabelbuku.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent event) {
                    if (!event.getValueIsAdjusting()) {
                        int selectedRow = tabelbuku.getSelectedRow();
                        if (selectedRow != -1) {
                            int idBuku = (int) model.getValueAt(selectedRow, 0);

                            String query = "SELECT * FROM buku WHERE id_buku = ?";
                            try {
                                PreparedStatement pstmt = koneksi.prepareStatement(query);
                                pstmt.setInt(1, idBuku);
                                ResultSet rs = pstmt.executeQuery();

                                if (rs.next()) {
                                    txtjudul.setText(rs.getString("judul"));
                                    txtpengarang.setText(rs.getString("pengarang"));
                                    txtpenerbit.setText(rs.getString("penerbit"));
                                    txtissbn.setText(rs.getString("issbn"));
                                    txtjumlah_salinan.setText(rs.getString("jumlah_salinan"));
                                    txtringkasan.setText(rs.getString("ringkasan"));
                                }
                            } catch (SQLException ex) {
                                ex.printStackTrace();
                                JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
                            }
                        }
                    }
                }
            });

            
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }
     
    }//GEN-LAST:event_btnloadActionPerformed

