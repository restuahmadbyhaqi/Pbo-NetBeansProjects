                model.addColumn("Penerbit");
                model.addColumn("ISSBN");
                model.addColumn("Jumlah Salinan");
                model.addColumn("Ringkasan");

                model.setRowCount(0);

                while (rs.next()) {
                    model.addRow(new Object[]{
                        rs.getInt("id_buku"),
                        rs.getString("judul"),
                        rs.getString("pengarang"),
                        rs.getString("penerbit"),
                        rs.getString("issbn"),
                        rs.getInt("jumlah_salinan"),
                        rs.getString("ringkasan")
                    });
                }
                
                txtjudul.setText("");
                txtpengarang.setText("");
                txtpenerbit.setText("");
                txtissbn.setText("");
                txtjumlah_salinan.setText("");
                txtringkasan.setText("");
